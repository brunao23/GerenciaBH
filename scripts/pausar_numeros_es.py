def pausar_numeros():
    """Pausa todos os números na tabela vox_es_pausar"""
    
    # Remover duplicatas
    numeros_unicos = list(set([n.strip() for n in numeros if n.strip()]))
    
    print(f"🔄 Iniciando processo de pausa de {len(numeros_unicos)} números únicos...")
    print(f"📋 Tabela: vox_es_pausar")
    print(f"📝 Motivo: sem ser vaga no cliente ES")
    print(f"ℹ️  Removidas {len(numeros) - len(numeros_unicos)} duplicatas\n")
    
    sucesso = 0
    erros = 0
    
    # Processar em lotes de 100 para melhor performance
    batch_size = 100
    total_batches = (len(numeros_unicos) + batch_size - 1) // batch_size
    
    for i in range(0, len(numeros_unicos), batch_size):
        batch = numeros_unicos[i:i + batch_size]
        batch_num = (i // batch_size) + 1
        
        print(f"📦 Processando lote {batch_num}/{total_batches} ({len(batch)} números)...")
        
        # Preparar dados para upsert (apenas campos essenciais)
        dados = []
        for numero in batch:
            numero_limpo = numero.strip()
            if numero_limpo:
                dados.append({
                    'numero': numero_limpo,
                    'pausar': True,
                    'vaga': False,
                    'agendamento': True
                })
        
        try:
            # Upsert em lote
            result = supabase.table('vox_es_pausar').upsert(
                dados,
                on_conflict='numero'
            ).execute()
            
            sucesso += len(dados)
            print(f"   ✅ Lote {batch_num} processado com sucesso ({len(dados)} números)")
            
        except Exception as e:
            erros += len(dados)
            print(f"   ❌ Erro no lote {batch_num}: {str(e)}")
    
    print(f"\n{'='*60}")
    print(f"✨ Processo concluído!")
    print(f"✅ Sucessos: {sucesso}")
    print(f"❌ Erros: {erros}")
    print(f"📊 Total: {len(numeros_unicos)}")
    print(f"{'='*60}")

if __name__ == "__main__":
    pausar_numeros()
